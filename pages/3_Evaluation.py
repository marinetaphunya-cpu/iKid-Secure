# เก็บค่าการเลือก
results = {}
for category, items in options.items():
    st.markdown("---")
    st.subheader(category)
    results[category] = st.radio(
        f"เลือกพฤติกรรมของ {category}",
        options=list(items.values()),
        index=0,
        key=category
    )

st.divider()

# ปุ่มดูผล (ลิงก์ไปหน้า Result ทันที)
if st.button("🚀 ดูผลการประเมิน (ไปยังหน้า Result)", use_container_width=True):
    # คำนวณคะแนนสูงสุด (เพื่อส่งไปหน้า Result)
    max_score = 0
    selected_labels = {}
    
    for category, items in options.items():
        chosen_label = results[category]
        selected_labels[category] = chosen_label
        for score, label in items.items():
            if chosen_label == label:
                if score > max_score:
                    max_score = score
    
    # ส่งข้อมูลไปหน้า Result
    st.session_state["evaluation_score"] = max_score
    st.session_state["evaluation_details"] = selected_labels
    
    # ย้ายหน้า
    st.switch_page("pages/4_Result.py")

